    private void loadtable() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Maskapai");
        model.addColumn("Dari");
        model.addColumn("Menuju");
        model.addColumn("Berangkat");
        model.addColumn("Tiba");
        model.addColumn("Tipe");
        model.addColumn("Harga");
        try {
            int no = 1;
            String sql = "SELECT * FROM jadwal";
            java.sql.Connection con = (Connection) cone.configDB();
            java.sql.Statement st = con.createStatement();
            java.sql.ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[]{no++, rs.getString(1), rs.getString(2),
                    rs.getString(3), rs.getString(4), rs.getString(5), 
                    rs.getString(6), rs.getString(7)});
            }
            jTable1.setModel(model);
            jTable2.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
